def selectable(auth: dict[str, str], round_trip: dict[str, str], manifest: dict[str, str]) -> bool:
    if auth.get("fixture_status") != "pending_authoring":
        return False
    if round_trip.get("fixture_status") != "pending_authoring":
        return False

    round_trip_required = round_trip.get("byte_identical_round_trip_required", "")
    operation_id = round_trip.get("expected_canonical_function_or_api_operation_id", "")
    if auth.get("cluster_scope") == "cluster_private":
        return (
            manifest.get("final_state") in {"exact_refusal_passed", "cluster_provider_route_passed"}
            and round_trip_required == "not_applicable_no_round_trip_in_public_build"
        )

    return (
        auth.get("status") == "native_now"
        and auth.get("cluster_scope") == "noncluster_or_profile_scoped"
        and manifest.get("final_state") == "e2e_passed"
        and round_trip_required == "yes"
        and operation_id
        and operation_id != "pending_canonical_authority_entry"
        and not operation_id.startswith("not_applicable_")
    )


def refreshable(auth: dict[str, str], round_trip: dict[str, str], manifest: dict[str, str]) -> bool:
    round_trip_required = round_trip.get("byte_identical_round_trip_required", "")
    operation_id = round_trip.get("expected_canonical_function_or_api_operation_id", "")
    if auth.get("cluster_scope") == "cluster_private":
        return (
            manifest.get("final_state") in {"exact_refusal_passed", "cluster_provider_route_passed"}
            and round_trip_required == "not_applicable_no_round_trip_in_public_build"
        )
    return (
        auth.get("status") == "native_now"
        and auth.get("cluster_scope") == "noncluster_or_profile_scoped"
        and manifest.get("final_state") == "e2e_passed"
        and round_trip_required == "yes"
        and operation_id
        and operation_id != "pending_canonical_authority_entry"
        and not operation_id.startswith("not_applicable_")
    )


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--repo-root", required=True)
    parser.add_argument("--artifact-root", default=DEFAULT_ARTIFACT_ROOT)
    parser.add_argument("--limit", type=int, default=50)
    parser.add_argument("--refresh", action="store_true")
    parser.add_argument(
        "--surface-id",
        action="append",
        default=[],
        help="Refresh a specific surface id from current matrices; may be repeated.",
    )
    args = parser.parse_args()
    root = Path(args.repo_root).resolve()
    artifact_root = Path(args.artifact_root)
    if not artifact_root.is_absolute():
        artifact_root = root / artifact_root
    if args.limit <= 0:
        fail("--limit must be positive")

    auth_rows = read_csv(artifact_root / AUTH_MATRIX_NAME)
    round_rows = read_csv(artifact_root / ROUND_MATRIX_NAME)
    manifest_rows = read_csv(artifact_root / PER_ROW_MANIFEST_NAME)

    auth_by_id = index_by_surface(auth_rows)
    round_by_id = index_by_surface(round_rows)
    manifest_by_id = index_by_surface(manifest_rows)

    selected: list[str] = []
    if args.surface_id:
        seen: set[str] = set()
        for surface_id in args.surface_id:
            if surface_id in seen:
                continue
            seen.add(surface_id)
            if surface_id not in auth_by_id or surface_id not in round_by_id or surface_id not in manifest_by_id:
                fail(f"requested surface id is not present in all fixture matrices: {surface_id}")
            if not refreshable(auth_by_id[surface_id], round_by_id[surface_id], manifest_by_id[surface_id]):
                fail(f"requested surface id is not eligible for fixture refresh: {surface_id}")
            selected.append(surface_id)
    else:
        for surface_id in sorted(set(auth_by_id) & set(round_by_id) & set(manifest_by_id)):
            if selectable(auth_by_id[surface_id], round_by_id[surface_id], manifest_by_id[surface_id]):
                selected.append(surface_id)
            if len(selected) >= args.limit:
                break
    if not selected:
        fail("no eligible pending rows found for fixture authoring")

    authored_route = 0
    authored_round_trip = 0
    for surface_id in selected:
        auth = auth_by_id[surface_id]
        round_trip = round_by_id[surface_id]
        manifest = manifest_by_id[surface_id]
        if write_fixture(root / auth["fixture_path"], route_fixture_rows(auth, manifest), args.refresh):
            authored_route += 1
        if write_fixture(root / round_trip["fixture_path"], round_trip_fixture_rows(round_trip, manifest), args.refresh):
            authored_round_trip += 1

    print(
        "sbsql_fixture_tranche=authored "
        f"selected={len(selected)} "
        f"authenticated_route_files={authored_route} "
        f"sblr_round_trip_files={authored_round_trip} "
        f"first={selected[0]} last={selected[-1]}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
