"""Author deterministic SBsql authenticated-route and SBLR round-trip fixtures.

This creates a bounded tranche of YAML fixtures from the generated matrices.
It does not promote row implementation state. Authored fixtures remain explicit
release risk until a later executable route/round-trip gate promotes them to
``e2e_passed``.
"""

from __future__ import annotations

import argparse
import csv
import json
import sys
from pathlib import Path


DEFAULT_ARTIFACT_ROOT = "project/tests/sbsql_parser_worker/fixtures/surface_to_sblr/artifacts"
AUTH_MATRIX_NAME = "AUTHENTICATED_FULL_ROUTE_MATRIX.csv"
ROUND_MATRIX_NAME = "SBLR_BINARY_ROUND_TRIP_MATRIX.csv"
PER_ROW_MANIFEST_NAME = "PER_ROW_EVIDENCE_MANIFEST.csv"


def fail(message: str) -> None:
    print(message, file=sys.stderr)
    raise SystemExit(1)


def read_csv(path: Path) -> list[dict[str, str]]:
    if not path.is_file():
        fail(f"required CSV missing: {path}")
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def index_by_surface(rows: list[dict[str, str]]) -> dict[str, dict[str, str]]:
    out: dict[str, dict[str, str]] = {}
    for row in rows:
        surface_id = row.get("surface_id", "")
        if surface_id in out:
            fail(f"duplicate surface_id in input: {surface_id}")
        out[surface_id] = row
    return out


def q(value: str) -> str:
    return json.dumps(value, ensure_ascii=True)


def write_fixture(path: Path, rows: list[tuple[str, str]], refresh: bool) -> bool:
    if path.exists() and not refresh:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    payload = "# Generated by author_route_and_round_trip_fixtures.py. Do not hand edit.\n"
    payload += "\n".join(f"{key}: {q(value)}" for key, value in rows)
    payload += "\n"
    path.write_text(payload, encoding="utf-8")
    return True


